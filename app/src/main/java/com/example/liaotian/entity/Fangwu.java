package com.example.liaotian.entity;

public class Fangwu {

    private int room_id;
    private String address;
    private int is_chuzu;
    private String time;
    private String zujin;
    private String fangshi;
    private String chaoxiang;
    private String tingshi;

    public String getZujin() {
        return zujin;
    }

    public void setZujin(String zujin) {
        this.zujin = zujin;
    }

    public String getFangshi() {
        return fangshi;
    }

    public void setFangshi(String fangshi) {
        this.fangshi = fangshi;
    }

    public String getChaoxiang() {
        return chaoxiang;
    }

    public void setChaoxiang(String chaoxiang) {
        this.chaoxiang = chaoxiang;
    }

    public String getTingshi() {
        return tingshi;
    }

    public void setTingshi(String tingshi) {
        this.tingshi = tingshi;
    }


    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getRoom_id() {
        return room_id;
    }

    public void setRoom_id(int room_id) {
        this.room_id = room_id;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getIs_chuzu() {
        return is_chuzu;
    }

    public void setIs_chuzu(int is_chuzu) {
        this.is_chuzu = is_chuzu;
    }
}
